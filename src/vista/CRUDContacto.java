    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        int confirmaBaja;

        try {
            conectarBase();
            int contactoBaja = Integer.parseInt(txtId_B.getText());

            confirmaBaja = stmt.executeUpdate("DELETE FROM tcliente WHERE id ='"+contactoBaja+"' " );

            if (confirmaBaja==1) {
                JOptionPane.showMessageDialog(null, "Se dio de baja el contacto de la base de datos " +contactoBaja+"\nVerifica Consulta");
                mostrarClientes();

            } else {
                JOptionPane.showMessageDialog(null, "Error! No existe el Id del contacto en la base de datos "+contactoBaja+"\nVerifica Consulta");
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al procesar baja de la Bases de datos\n\n"+ex);
        }
        
        
        
    }//GEN-LAST:event_btnEliminarActionPerformed

