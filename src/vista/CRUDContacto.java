                + "VALUES('"+objCliente.getId()+"','"
                +objCliente.getNombre()+"','"
                +objCliente.getDireccion()+"','"
                +objCliente.getPais()+"','"
                +objCliente.getCorreo()+"','"
                +objCliente.getTelefono()+"','"
                +objCliente.getCodigoPostal()+"','"
                +objCliente.getTipoCliente()+"','"
                +objCliente.getPuesto()+"','"
                +objCliente.getNombreEmpresa()+"','"
                +objCliente.getGiro()+"','"
                +objCliente.getObservaciones()+"')";
        
        //estructura de control de tratamiento de errores en ejecucion
        try{
            stmt.executeUpdate(altaCliente);
            JOptionPane.showMessageDialog(null, "Se registro cliente de forma exitosa "+objCliente.getNombre());
            mostrarClientes();
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error 1 de BD al generar alta Libro\n\nVerifica\n\n"+ e);
        }


