    public String convertirMayusculas(String palabra){
        
        String firstLtr = palabra.substring(0, 1);
        String restLtrs = palabra.substring(1, palabra.length());
        firstLtr = firstLtr.toUpperCase();
        palabra = firstLtr+restLtrs;
        
        return palabra;
    }
    
    
