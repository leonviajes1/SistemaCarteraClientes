    private void btnEntrarCMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarCMActionPerformed

        conectarBase(); //LLamada a conexi√≥n de BD

        if (optAdministrador.isSelected()) { //if para usuario administrador

            try {
                String usuarioAcceso = txtUsuario.getText();
                String passwordAcceso = txtPassword.getText();

                boolean error = true;
                rs=stmt.executeQuery("SELECT * from tusuariosadmin");
                while(rs.next()==true){
                    if(usuarioAcceso.equals(rs.getString("usuario_admin"))&&passwordAcceso.equals(rs.getString("password_admin"))){
                        error=false;
                        
                        //Guarda el usuario en la clase Temporal
                        Temporal tempo = new Temporal();
                        tempo.setUsuario(usuarioAcceso);
                        tempo.setTipoUsuario("administrador");

                        CRUDContacto ventanaCRUDContacto = new CRUDContacto();
                       // JOptionPane.showMessageDialog(null, "Bienvenido a sistema Administrador\n"+usuarioAcceso);
                        ventanaCRUDContacto.setVisible(true);
                        this.hide();

                    }//termina if
                }//termina while
                if(error ==true){//if si no encontro a usuario
                    JOptionPane.showMessageDialog(null, "Error al ingresar usuario\nVerifica!!!");
                    txtUsuario.setText(usuarioAcceso);
                    txtPassword.setText(null);
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,"Error de DB verifica"+ e);
            }//Termina catch

        } else if (optVisitante.isSelected()){ //If para usuario vendedor

            try {
                String usuarioAcceso = txtUsuario.getText();
                String passwordAcceso = txtPassword.getText();

                boolean error = true;
                rs=stmt.executeQuery("SELECT * from tusuariosvisit");
                while(rs.next()==true){
                    if(usuarioAcceso.equals(rs.getString("usuario_visit"))&&passwordAcceso.equals(rs.getString("password_visit"))){
                        error=false;
                        
                        //Guarda el usuario en la clase Temporal
                        Temporal tempo = new Temporal();
                        tempo.setUsuario(usuarioAcceso);
                        tempo.setTipoUsuario("visitante");
                        
                        

                        CRUDContacto ventanaCRUDContacto = new CRUDContacto();
                        //JOptionPane.showMessageDialog(null, "Bienvenido a sistema Visitante\n"+usuarioAcceso);
                        ventanaCRUDContacto.setVisible(true);
                        this.hide();

                    }//termina if
                }//termina while
                if(error ==true){//if si no encontro a usuario
                    JOptionPane.showMessageDialog(null, "Error al ingresar usuario\nVerifica!!!");
                    txtUsuario.setText(usuarioAcceso);
                    txtPassword.setText(null);
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,"Error de DB verifica"+ e);
            }

        }

    }//GEN-LAST:event_btnEntrarCMActionPerformed

